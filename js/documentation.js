/**
 * @description returns sum of two elements
 * @param  {number} first component of sum
 * @param {number} second component of sum
 * @return {number} sum of two components
 */
//typ wartości w nawiasach, nazwa parametru, którego typ dotyczy, komentarz pod linią
function sum(first, second)
{
    return first + second;
}


console.log(sum(3,2));